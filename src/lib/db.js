import { openDB } from 'idb'

// Local database (IndexedDB): all expenses and photos stay on the device.
// Nothing is sent to a server (except the image sent to Gemini at analysis time).
const DB_NAME = 'spend-manager'
const DB_VERSION = 2

let dbPromise

function getDB() {
  if (!dbPromise) {
    dbPromise = openDB(DB_NAME, DB_VERSION, {
      upgrade(db) {
        if (!db.objectStoreNames.contains('expenses')) {
          const store = db.createObjectStore('expenses', { keyPath: 'id' })
          store.createIndex('date', 'date')
        }
        if (!db.objectStoreNames.contains('images')) {
          // key = imageId, value = compressed JPEG Blob
          db.createObjectStore('images')
        }
        if (!db.objectStoreNames.contains('analyses')) {
          // AI analyses of the expenses (observation + joke) generated by Gemini
          db.createObjectStore('analyses', { keyPath: 'id' })
        }
      },
    })
  }
  return dbPromise
}

export async function getAllExpenses() {
  const db = await getDB()
  const all = await db.getAll('expenses')
  // Most recent first (by date, then by creation order)
  return all.sort((a, b) => {
    if ((a.date || '') !== (b.date || '')) return (a.date || '') < (b.date || '') ? 1 : -1
    return (b.createdAt || 0) - (a.createdAt || 0)
  })
}

export async function saveExpense(expense) {
  const db = await getDB()
  await db.put('expenses', expense)
}

export async function deleteExpense(id) {
  const db = await getDB()
  const expense = await db.get('expenses', id)
  await db.delete('expenses', id)
  if (expense?.imageId) {
    await db.delete('images', expense.imageId)
  }
}

export async function saveImage(id, blob) {
  const db = await getDB()
  await db.put('images', blob, id)
}

export async function getImage(id) {
  if (!id) return null
  const db = await getDB()
  return db.get('images', id)
}

// --- AI analyses (observation + joke generated from the summary) ---

export async function getAllAnalyses() {
  const db = await getDB()
  const all = await db.getAll('analyses')
  // Most recent first
  return all.sort((a, b) => (b.createdAt || 0) - (a.createdAt || 0))
}

export async function saveAnalysis(analysis) {
  const db = await getDB()
  await db.put('analyses', analysis)
}

export async function deleteAnalysis(id) {
  const db = await getDB()
  await db.delete('analyses', id)
}

export async function clearAll() {
  const db = await getDB()
  await db.clear('expenses')
  await db.clear('images')
  await db.clear('analyses')
}
